    //Send Email button
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String uname = jTextField1.getText();
        
        String usernameRegex = "^[\\w!#$%&'*+/=?`{|}~^-]+(?:\\.[\\w!#$%&'*+/=?`{|}~^-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,6}$";
        Pattern usernamePattern = Pattern.compile(usernameRegex);
        Matcher usernameMatcher = usernamePattern.matcher(uname);
        
        if(!usernameMatcher.matches()) {
            JOptionPane.showMessageDialog(null, "Please enter a valid username.", "Username Invalid", 2);
        } else {
            try {
                //Prepare user check query
                PreparedStatement userCheckPS;
                ResultSet userCheckRS;
                String userCheckQuery = "SELECT * FROM Users WHERE Username=?";

                userCheckPS = MyConnection.getConnection().prepareStatement(userCheckQuery);
                userCheckPS.setString(1, uname);

                userCheckRS = userCheckPS.executeQuery();
                
                if(userCheckRS.next()) {
                    this.setUsername(uname);
                    this.setUserID(userCheckRS.getInt("UserID"));
                    //Generate random login code for user (six-digit)
                    int randomCode = new Random().nextInt(900000) + 100000;
                    this.setCode(randomCode);
                    
                    //Send email
                    //https://stackoverflow.com/questions/46663/how-can-i-send-an-email-by-java-application-using-gmail-yahoo-or-hotmail
                    String recipient = userCheckRS.getString("Username");
                    
                    Properties properties = System.getProperties();
                    String host = "smtp.gmail.com";
                    properties.put("mail.smtp.starttls.enable", "true");
                    properties.put("mail.smtp.host", host);
                    properties.put("mail.smtp.user", this.getAccountUsername());
                    properties.put("mail.smtp.password", this.getAccountPassword());
                    properties.put("mail.smtp.port", "587");
                    properties.put("mail.smtp.auth", "true");
                    
                    Session session = Session.getDefaultInstance(properties);
                    MimeMessage message = new MimeMessage(session);
                    
                    try {
                        // Set From: header field of the header.
                        message.setFrom(new InternetAddress(this.getAccountUsername()));

                        // Set To: header field of the header.
                        message.addRecipient(Message.RecipientType.TO, new InternetAddress(recipient));

                        // Set Subject: header field
                        message.setSubject("[no-reply] Forgot Password - Automated Login Code");

                        // Now set the actual message
                        message.setText("Enter this code into application: " + this.getCode());

                        // Send message
                        Transport transport = session.getTransport("smtp");
                        transport.connect(host, this.getAccountUsername(), this.getAccountPassword());
                        transport.sendMessage(message, message.getAllRecipients());
                        transport.close();
                    } catch (MessagingException mex) {
                        mex.printStackTrace();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Username not in use by any account.", "Login Failed", 2);
                }
            } catch(SQLException e) {
                Logger.getLogger(ForgotPasswordEmail.class.getName()).log(Level.SEVERE, null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

